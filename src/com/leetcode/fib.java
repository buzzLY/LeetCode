package com.leetcode;

public class fib {
	
	public static void _fib() {
		int count = 0;
		while(true) {
			count++;
			if(count==10) {
				break;
			}
			else {
				continue;
			}
		}
		System.out.println(count);
	}
	
	public static void main(String[] args) {
		fib._fib();
		float x = 5;
		if(x>1) {
			System.out.println("The game");
		}
		else {
			System.out.println("We Won");
		}
	}
}
